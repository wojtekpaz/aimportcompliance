#  Submit (client) — Section 8 resume                                          #
# --------------------------------------------------------------------------- #

@router.post("/survey/{token}/submit")
def survey_submit(token: str, body: SurveySubmitIn):
    sess = sdb.get_session(token)
    if sess is None:
        return JSONResponse({"error": "not found"}, status_code=404)
    if sess.get("expired"):
        return JSONResponse({"error": "expired"}, status_code=410)
    if sess.get("status") == "completed":
        return JSONResponse({"error": "already submitted"}, status_code=409)

    if body.client_email:
        sdb.set_client_email(token, body.client_email)

    resolved_any = False
    for a in body.answers:
        q = sdb.get_question(a.question_id)
        if not q or q["session_id"] != token:
            continue
        options = q["option_set"] or []
        answer = (a.answer or "").strip()
        detail = (a.answer_detail or "").strip()

        # GRI guardrail: the answer must be one of the engine's options, unless
        # it's a free-text-trigger option (which is itself in the option set).
        if answer and answer not in options:
            # tolerate an option the client typed verbatim differences-aside
            match = next((o for o in options if o.lower() == answer.lower()), None)
            answer = match or answer
        if answer not in options and not detail:
            # not a valid selection and no supplementary text -> leave frozen
            sdb.record_answer(q["id"], a.answer or "", detail)
            sdb.write_result(token, q["id"], q["line_number"], None, True,
                             "Answer was not one of the offered options.")
            continue

        sdb.record_answer(q["id"], answer, detail)
        final_code, still_frozen, notes = _resolve_question(q, answer, detail, market="EU")
        if not still_frozen:
            resolved_any = True
        sdb.write_result(token, q["id"], q["line_number"], final_code,
                         still_frozen, notes)

    sdb.mark_completed(token)

    # Optional broker notification (Section 4.4): plain text, no invoice data.
    notified = False
    if sess.get("client_email") or sess.get("broker_id"):
        notified = _notify_broker(sess, token)
    if notified:
        sdb.mark_notification_sent(token)

    return {"ok": True, "resolved_any": resolved_any}


def _resolve_question(q: dict, answer: str, detail: str, market="EU"):
    """Resume the GRI engine for one frozen line. Returns
    (final_code|None, still_frozen: bool, notes: str)."""
    snap = q["engine_state"] or {}
    kind = snap.get("kind")
    sig = snap.get("sig")

    try:
        if kind == "node":
            option_map = snap.get("option_map") or {}
            chosen_text = answer
            # Section 8.6: supplementary free text -> oracle picks an option.
            if detail and (answer in _FREETEXT_TRIGGERS or answer not in option_map):
                picked = stext.pick_option_for_freetext(
                    detail, list(option_map.keys()), market)
                if picked:
                    chosen_text = picked
            opt_id = option_map.get(chosen_text)
            if opt_id is None:
                return None, True, ("Needs manual review: the client's answer did "
                                    "not map to an engine option.")
            res = es.resume(snap, sig, opt_id)

        elif kind == "pre_classify":
            # The meaningful content is the supplementary description text.
            choice = detail or (answer if answer not in _FREETEXT_TRIGGERS else "")
            if not choice:
                return None, True, ("Needs manual review: client offered a photo / "
                                    "no description text was provided.")
            res = es.resume(snap, "__pre_classify__", choice)

        elif kind == "ocr_confirm":
            # Confirm or correct the unreadable field, then classify the line.
            confirmed = detail if answer == OCR_WRONG_OPTION else answer
            text = snap.get("text", "") or ""
            origin = snap.get("origin", "") or ""
            if not text.strip():
                return None, True, ("Needs manual review: confirmed value "
                                    f"'{confirmed}' but no description to classify.")
            res = es.start(text, origin, "")

        else:
            return None, True, "Needs manual review: unknown freeze type."
    except Exception as e:
        return None, True, f"Needs manual review: engine error ({str(e)[:80]})."

    status = res.get("status")
    if status == "classified":
        return res.get("code"), False, ""
    if status in ("needs_question", "needs_pre_classify"):
        second_q = res.get("question")
        if isinstance(second_q, dict):
            second_q = second_q.get("ask") or second_q.get("why") or "further detail"
        return None, True, f"Needs manual review: {second_q}"
    return None, True, (res.get("message") or "Needs manual review.")


def _notify_broker(sess: dict, token: str) -> bool:
    """Section 4.4 — a plain-text 'survey complete' notification with NO invoice
    data in the body. This milestone does not send mail from the backend
    (Section 10), so we only record intent and log; real delivery is a later
    milestone. Returns False (nothing actually sent) but is safe to call."""
    try:
        ref = sess.get("invoice_ref") or "your invoice"
        msg = f"Survey complete — {ref} is ready for review."
        import logging
        logging.getLogger("uvicorn.error").info("[survey] %s (token %s)",
                                                 msg, token[:8])
    except Exception:
        pass
    return False  # backend e-mail sending is intentionally out of scope here


