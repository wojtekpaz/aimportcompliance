#!/usr/bin/env python3
"""
survey_api.py — Client-clarification survey routes for AImport Compliance.

Milestone One (V2), Sections 4–8. All routes are additive and registered on the
main app via include_router(); the existing classify/products/invoice routes are
untouched.

  POST /survey/create            broker -> create a survey from frozen lines
  GET  /survey/{token}           client -> the survey page (or expired/submitted)
  GET  /survey/{token}/data      client -> simplified, sanitised questions (JSON)
  POST /survey/{token}/submit    client -> answers; engine resumes per line
  GET  /survey/{token}/results   broker -> resolved + still-frozen lines (JSON)

Architectural guardrails enforced here:
  * option_set per question is always populated; the client never has a free-text
    path that yields a code directly.
  * The GRI state machine owns all sequencing on resume (engine_session.resume).
  * The oracle's only generative roles are (a) simplifying a question's wording,
    (b) selecting from an option set for supplementary free text. No code is ever
    generated by an LLM.
  * /survey/{token}/data never exposes codes, engine state, duty rates, or other
    clients' data — the client sees only their own line items.
"""
import sys
from pathlib import Path

from fastapi import APIRouter, Request
from fastapi.responses import FileResponse, JSONResponse
from pydantic import BaseModel

HERE = Path(__file__).resolve().parent
sys.path.insert(0, str(HERE))
import engine_session as es        # noqa: E402
import survey_db as sdb            # noqa: E402
import survey_text as stext        # noqa: E402
from survey_freeze import FREETEXT_OPTION, OCR_WRONG_OPTION  # noqa: E402

router = APIRouter()

# Options that, when chosen, reveal a single constrained free-text field.
_FREETEXT_TRIGGERS = {FREETEXT_OPTION, OCR_WRONG_OPTION}


# --------------------------------------------------------------------------- #
#  Models                                                                      #
# --------------------------------------------------------------------------- #

class FrozenLineIn(BaseModel):
    line_number: int = 0
    description_used: str | None = None
    hs_code_attempted: str | None = None
    freeze_reason: str = "NEEDS_DETAIL"
    engine_question: str = ""
    option_set: list = []
    engine_state_snapshot: dict = {}
    partial_heading: str | None = None


class SurveyCreateIn(BaseModel):
    session_broker_id: str = ""
    invoice_ref: str = ""
    frozen_lines: list[FrozenLineIn] = []
    client_email: str = ""
    language: str = "pl"          # client-facing survey language (Polish default)


class AnswerIn(BaseModel):
    question_id: str
    answer: str = ""
    answer_detail: str = ""


class SurveySubmitIn(BaseModel):
    answers: list[AnswerIn] = []
    client_email: str = ""


# --------------------------------------------------------------------------- #
#  Create (broker)                                                             #
# --------------------------------------------------------------------------- #

@router.post("/survey/create")
def survey_create(body: SurveyCreateIn, request: Request):
    frozen = [fl.model_dump() for fl in body.frozen_lines]
    if not frozen:
        return JSONResponse({"error": "No frozen lines — nothing to ask."},
                            status_code=400)
    created = sdb.create_session(
        broker_id=body.session_broker_id,
        invoice_ref=body.invoice_ref,
        frozen_lines=frozen,
        client_email=body.client_email,
        language=body.language or "pl",
    )
    token = created["token"]
    # Prefer the public base URL (hosted deployment) so the emailed link is
    # reachable by the client, not the broker's localhost.
    import os
    base = ((os.environ.get("PUBLIC_BASE_URL") or "").strip().rstrip("/")
            or str(request.base_url).rstrip("/"))
    return {"survey_token": token,
            "survey_url": f"{base}/survey/{token}"}


# --------------------------------------------------------------------------- #
#  Render (client)                                                             #
# --------------------------------------------------------------------------- #

@router.get("/survey/{token}")
def survey_page(token: str):
    sess = sdb.get_session(token)
    if sess is None or sess.get("expired"):
        return FileResponse(HERE / "survey_expired.html", status_code=404
                            if sess is None else 410)
    if sess.get("status") == "completed":
        return FileResponse(HERE / "survey_already_submitted.html")
    return FileResponse(HERE / "survey_form.html")


@router.get("/survey/{token}/data")
def survey_data(token: str):
    """Sanitised, client-facing question payload in the session's language
    (Polish by default). No codes, no engine state. The radio `value` the client
    submits stays the original (English) engine option; only the display label
    and context line are localised."""
    sess = sdb.get_session(token)
    if sess is None or sess.get("expired"):
        return JSONResponse({"status": "expired"}, status_code=410)
    if sess.get("status") == "completed":
        return JSONResponse({"status": "completed"})

    language = (sess.get("language") or "pl")
    questions = sdb.get_questions(token)
    total = len(questions)
    out = []
    for i, q in enumerate(questions, start=1):
        options = q["option_set"] or []
        out.append({
            "question_id": q["id"],
            "index": i,
            "total": total,
            "question": stext.simplify_question(q["engine_question"], language),
            # English option VALUES (submitted to the engine) ...
            "options": options,
            # ... and parallel localised display LABELS
            "option_labels": [stext.translate_option(o, language) for o in options],
            # which options open a single constrained free-text field
            "freetext_options": [o for o in options if o in _FREETEXT_TRIGGERS],
            "freeze_reason": q.get("freeze_reason"),
            "context_label": stext.freeze_reason_label(q.get("freeze_reason"), language),
        })
    return {"status": "pending", "total": total, "language": language,
            "intro": stext.survey_intro(language),
            "questions": out}


