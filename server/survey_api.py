@router.get("/survey/{token}/results")
def survey_results(token: str):
    sess = sdb.get_session(token)
    if sess is None:
        return JSONResponse({"error": "not found"}, status_code=404)
    questions = {q["id"]: q for q in sdb.get_questions(token)}
    results = sdb.get_results(token)
    lines = []
    for r in results:
        q = questions.get(r["question_id"], {})
        lines.append({
            "line_number": r["line_number"],
            "description": q.get("description_used"),
            "hs_code_attempted": q.get("hs_code_attempted"),
            "freeze_reason": q.get("freeze_reason"),
            "engine_question": q.get("engine_question"),
            "answer": q.get("answer"),
            "answer_detail": q.get("answer_detail"),
            "final_code": r["final_code"],
            "still_frozen": bool(r["still_frozen"]),
            "resolution_notes": r["resolution_notes"],
        })
    return {
        "token": token,
        "invoice_ref": sess.get("invoice_ref"),
        "broker_id": sess.get("broker_id"),
        "status": sess.get("status"),
        "created_at": sess.get("created_at"),
        "expires_at": sess.get("expires_at"),
        "expired": sess.get("expired"),
        "lines": lines,
    }


